Temperature = 25

def thermostat_regulator(temperature, low_temp = 20, high_temp = 27):
    if temperature <= low_temp:
        state = 'Turn on heater'
    elif temperature >= high_temp:
        state = 'Turn on cooler'
    else:
        state = 'Do nothing'
    print(state)
    return state


state = thermostat_regulator(Temperature)